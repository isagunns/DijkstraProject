                for (int v = 0; v < n; v++)
                {//ziyaret edilmemişse ve rota min değerden küçük veya eşitse
                    if (visited[v] == false && distance[v] <= min)
                    {
                        min = distance[v];
                        minIndex = v;
                    }
                }

                return minIndex;//kaynak şehrirden en kısa uzaklıktaki şehrin indeksini döndürür
            }
