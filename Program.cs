            //dijkstra algoritması
             int Dijkstra(int[][] graph, int source, int destination)
            {
                int n = graph.Length;
                int[] mesafe = new int[n];
                bool[] ziyaretEdilen = new bool[n];


                for (int i = 0; i < n; i++)
                {
                    mesafe[i] = int.MaxValue;//kaynak şehrin diğer şehirlere uzaklığını başlangıçta maximum değer veriyoruz
                    ziyaretEdilen[i] = false;//başlangıçta hiçbir şehri ziyaret etmedi olarak ayarlıyoruz
                }

                mesafe[source] = 0; //şehrin kendine uzaklığı 0 dır

                for (int count = 0; count < n; count++)
                {
                    int u = MinDistance(mesafe, ziyaretEdilen, n); //kaynak şehirden en kısa uzaklıktaki şehrin indeksini bulur
                    ziyaretEdilen[u] = true;

                    for (int v = 0; v < graph[u].Length; v++)
                    {
                        //ziyaret edilmemişse ve en yakın şehir ile indeksteki şehir komşu ise(max value değil) ve kaynak şehire en kısa mesafedeki şehir max value değilse ve kaynak şehre en yakın şehrin uzaklığı(u şehri) ile u şehrinden ineksteki şehirin uzaklığının toplamı kaynak şehrin indeksteki şehre uzaklığından küçük ise 
                        if (!ziyaretEdilen[v] && graph[u][v] != int.MaxValue && mesafe[u] != int.MaxValue && mesafe[u] + graph[u][v] < mesafe[v])//kısacası son kısım ile en kısa rotayı bulduruyoruz
                        {
                            mesafe[v] = mesafe[u] + graph[u][v];
                        }
                    }
                }

                return mesafe[destination];//kaynak şehirden gidilmek istenen şehre en kısa rotadan giden mesafeyi dönderir
            }

